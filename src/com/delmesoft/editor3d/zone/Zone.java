package com.delmesoft.editor3d.zone;

import com.delmesoft.editor3d.entity.ObjInfo;

import com.badlogic.gdx.graphics.Camera;
import com.badlogic.gdx.math.Intersector;
import com.badlogic.gdx.math.Vector3;
import com.badlogic.gdx.math.collision.Ray;

public class Zone extends ObjInfo {

	public Vector3 position;
	public Vector3 dimensions;
	
	public Zone() {
		this.position   = new Vector3();
		this.dimensions = new Vector3();
	}

	public boolean hit(Ray ray) {
		return Intersector.intersectRaySphere(ray, position, /*light.intensity*/ 0.5F, null);
	}

	public boolean isVisible(Camera camera, float maxDistance) {
		return camera.position.dst(position) < maxDistance && camera.frustum.boundsInFrustum(position.x, position.y, position.z, dimensions.x * 0.5F, dimensions.y * 0.5F, dimensions.z * 0.5F);
	}

}
